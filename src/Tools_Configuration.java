    String fileDialog(String title)
    {
        String path=null;
        java.awt.FileDialog fd = new java.awt.FileDialog(this,title, FileDialog.LOAD);
         fd.setVisible(true);
         if(fd.getFile()!=null)
         {
            path = fd.getDirectory()+"/"+fd.getFile();
         }
         else
         {
             path="";
         }
        return path;
    }
