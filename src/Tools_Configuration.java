    public void initialValues()
    {
        if(pro.javaExecute!=null)
        {
            this.jTextField1.setText(pro.javaExecute);
            this.jLabel5.setText("");
        }
        else
        {
            this.jLabel5.setText("*");
        }
        if(pro.javaCompiler!=null)
        {
            this.jTextField2.setText(pro.javaCompiler);
            this.jLabel6.setText("");
        }
        else
        {
            this.jLabel6.setText("*");
        }
        if(pro.javaAppletViewer!=null)
        {
            this.jTextField3.setText(pro.javaAppletViewer);
            this.jLabel7.setText("");
        }
        else
        {
            this.jLabel7.setText("*");
        }
        if(pro.cppCompiler!=null)
        {
            this.jTextField4.setText(pro.cppCompiler);
            this.jLabel8.setText("");
        }
        else
        {
            this.jLabel8.setText("*");
        }
    }
    public void save(String language)
    {
        String s= null;
        if(language.equals("java"))
        {
            s = this.jTextField1.getText();
            if((!s.equals("")))
            { 
                if((new File(s).exists()||new File(s+".exe").exists()))
                {
                    pro.javaExecute = s;
                    this.jLabel5.setText("");
                }
                else
                {
                    this.jLabel5.setText("*");
                }
            }
            else
            {
                this.jLabel5.setText("");
                pro.javaExecute = null;
            }
            s = this.jTextField2.getText();
            if((!s.equals("")))
            {
                if((new File(s).exists()||new File(s+".exe").exists()))
                {
                    pro.javaCompiler = s;
                    this.jLabel6.setText("");
                }
                else
                {
                    this.jLabel6.setText("*");
                }
            }
            else
            {
                this.jLabel6.setText("");
                pro.javaCompiler = null;
            }
            s = this.jTextField3.getText();
            if((!s.equals("")))
            {
                if((new File(s).exists()||new File(s+".exe").exists()))
                {
                    pro.javaAppletViewer = s;
                    this.jLabel7.setText("");
                }
                else
                {
                    this.jLabel7.setText("*");
                }
            }
            else
            {
                pro.javaAppletViewer = null;
                this.jLabel7.setText("");
            }
        }
        else if(language.equals("cpp"))
        {
            s = this.jTextField4.getText();
            if((!s.equals("")))
            {
                if((new File(s).exists()||new File(s+".exe").exists()))
                {
                    pro.cppCompiler = s;
                    pro.cppCompilerType= choice1.getSelectedIndex();
                    this.jLabel8.setText("");
                }
                else
                {
                    this.jLabel8.setText("*");
                }
            }
            else
            {
                pro.cppCompiler = null;
                pro.cppCompilerType =-1;
                this.jLabel8.setText("");
            }
        }
    }
    
